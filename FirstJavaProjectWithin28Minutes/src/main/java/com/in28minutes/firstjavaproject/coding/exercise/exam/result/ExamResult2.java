package com.in28minutes.firstjavaproject.coding.exercise.exam.result;

public class ExamResult2 {
    // Method to check if a student has passed or failed based on their marks
    public boolean isPass(int marks) {
        // If the student's marks are greater than 50, they have passed the exam.
        // This will return true for passing marks and false for failing marks.
        return marks > 50;
    }
}
